public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setXCoordinate(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setYCoordinate(double y) {
        this.y = y;
    }
}
